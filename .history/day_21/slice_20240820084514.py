# slicing:  Specify the start index and the end index, separated by a colon, to return a part of the string. 
# For example, "hello"[1:3] returns "el".
# string slicing is zero-based, meaning the first character is at index 0.
# If you omit the start index, Python will start from the beginning of the string. 
# If you omit the end index, Python will go to the end of the string.
# string slicing can be used to extract a substring from a string.
