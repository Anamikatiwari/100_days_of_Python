# wap that automatically prints the solution to the FizzBuzz game. 

for number in range(1, 101):
    if number % 3 == 0 and number % 5 == 0:
        print("FizzBuzz")
    elif number % 3 == 0:
        print("Fizz")
    elif number % 5 == 0:
        print("Buzz")
    else:
        print(number)  # print the number if it doesn't meet any of the above conditions.